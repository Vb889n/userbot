@jmthon.ar_cmd(
    pattern="بنك( معدل|$)",
    command=("بنك", plugin_category),
    info={
        "header": "لاظهار لك البنك الخاص بـ بوتك",
        "flags": {"معدل": "معدل المتوسط للبنك"},
        "usage": ["{tr}بنك", "{tr}بنك معدل"],
    },
)
async def _(event):
    "لحساب البنك"
    flag = event.pattern_match.group(1)
    start = datetime.now()
    if flag == " معدل":
        jmthonevent = await edit_or_reply(event, "`!....`")
        await asyncio.sleep(0.3)
        await jmthonevent.edit("`..!..`")
        await asyncio.sleep(0.3)
        await jmthonevent.edit("`....!`")
        end = datetime.now()
        tms = (end - start).microseconds / 1000
        ms = round((tms - 0.6) / 3, 3)
        await jmthonevent.edit(f"معدب البنك!\n`{ms} ms`")
    else:
        jmthonevent = await edit_or_reply(event, "بنك!")
        end = datetime.now()
        ms = (end - start).microseconds / 1000
        await jmthonevent.edit(f"البنك!\n`{ms} ms`")


